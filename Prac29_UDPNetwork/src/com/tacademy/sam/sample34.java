package com.tacademy.sam;

public class sample34 {
	private int no;
	private int price;
	private int quantity;
	
	
	
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	
	
	@Override
	public String toString() {
		return "sample34 [no=" + no + ", price=" + price + ", quantity=" + quantity + "]";
	}
	
	
	
}
